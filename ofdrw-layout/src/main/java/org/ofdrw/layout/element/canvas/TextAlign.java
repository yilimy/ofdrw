package org.ofdrw.layout.element.canvas;

/**
 * 文本内容的当前对齐方式
 *
 * @author 权观宇
 * @since 2020-05-10 18:05:47
 */
public enum TextAlign {

    /**
     * 文本在指定的位置开始
     */
    start,
    /**
     * 文本在指定的位置结束。
     */
    end,
    /**
     * 文本的中心被放置在指定的位置
     */
    center,
    /**
     * 文本左对齐
     */
    left,
    /**
     * 文本右对齐
     */
    right;
}
